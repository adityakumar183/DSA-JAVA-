package BasicsOfJava;

public class arithmeticprog {
    public static void main(String[] args) {
        double x=10;
        double y=20;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}

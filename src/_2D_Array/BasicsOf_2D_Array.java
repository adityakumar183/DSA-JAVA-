package _2D_Array;

public class BasicsOf_2D_Array {
    public static void main(String[] args) {
        int [][] arr=new int[2][3];
        // 10 20 30
        // 40 50 60
        arr[0][0]=10;
        arr[0][1]=20;
        arr[0][2]=30;

        System.out.println(arr[0][2]);
        System.out.println(arr[1][2]);
    }
}
